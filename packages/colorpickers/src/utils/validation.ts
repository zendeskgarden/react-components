export const isValidHex = (hexString: string) => {
  const regEx = /^#(?:(?:[0-9A-F]{6}(?:[0-9A-F]{2})?)|(?:[0-9A-F]{3})(?:[0-9A-F]?))$/iu;

  return regEx.test(hexString);
};
